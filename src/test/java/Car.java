public class Car {
    //----------------------ATTRIBUTES---------------------
    private Integer weight;
    private String name;
    private Character vw;
    private Float itemCount;
    private double slim;

    //----------------------GETTERS/SETTERS---------------------


    public Integer getWeight() {
        return weight;
    }

    public void setWeight(Integer weight) {
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Character getVw() {
        return vw;
    }

    public void setVw(Character vw) {
        this.vw = vw;
    }

    public Float getItemCount() {
        return itemCount;
    }

    public void setItemCount(Float itemCount) {
        this.itemCount = itemCount;
    }

    public double getSlim() {
        return slim;
    }

    public void setSlim(double slim) {
        this.slim = slim;
    }
}
