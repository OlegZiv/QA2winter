public class Door {
    //--------------------ATTRIBUTES-------------------
    private String color;
    private double price;
    private String brand;
    private String withGlass;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    private char itemCount;
    private String name;

    public String getRed() {
        return color;
    }

    public void setRed(String red) {
        this.color = red;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getWithGlass() {
        return withGlass;
    }

    public void setWithGlass(String withGlass) {
        this.withGlass = withGlass;
    }

    public char getItemCount() {
        return itemCount;
    }

    public void setItemCount(char itemCount) {
        this.itemCount = itemCount;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
